package com.led.player.po;

public class GPSDemandImagePo {
	public int _protype;
	public int _prolen;
	public int _transparent;
	public int _effects;
	public int _txtspeed;
	public int _description;
	public int _stayline;
	public String _bgclr;
	public String _filepath;
	
	public String _begintime;
	public String _coordinate;
	public String _coverradius;
	
	public GPSDemandImagePo() {}

	public GPSDemandImagePo(int _protype, int _prolen, int _transparent,
			int _effects, int _txtspeed, int _description, int _stayline,
			String _bgclr, String _filepath, String _begintime,
			String _coordinate, String _coverradius) {
		super();
		this._protype = _protype;
		this._prolen = _prolen;
		this._transparent = _transparent;
		this._effects = _effects;
		this._txtspeed = _txtspeed;
		this._description = _description;
		this._stayline = _stayline;
		this._bgclr = _bgclr;
		this._filepath = _filepath;
		this._begintime = _begintime;
		this._coordinate = _coordinate;
		this._coverradius = _coverradius;
	}

	@Override
	public String toString() {
		return "GPSTimeDemandImagePo [_protype=" + _protype + ", _prolen="
				+ _prolen + ", _transparent=" + _transparent + ", _effects="
				+ _effects + ", _txtspeed=" + _txtspeed + ", _description="
				+ _description + ", _stayline=" + _stayline + ", _bgclr="
				+ _bgclr + ", _filepath=" + _filepath + ", _begintime="
				+ _begintime + ", _coordinate=" + _coordinate
				+ ", _coverradius=" + _coverradius + "]";
	}

	
	
	
}
