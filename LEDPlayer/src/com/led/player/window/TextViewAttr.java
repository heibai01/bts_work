package com.led.player.window;

import android.graphics.Color;
/**
 * 文字部分公用的一些属性。
 * @author 1231
 *
 */
public class TextViewAttr {

	public String Content = "这是默认测试文字。。。请发送文字";
	public int FixedWordClr = Color.RED;
	public int WordClr = Color.RED;
	public int BgClr = Color.BLACK;
//	int BgClr = Color.RED;  //这个字段没改掉看来。
	public int Italic = 0;
	public int Underline = 0;
	public int Height = 10;
	public int FaceName = 0;
	public int Weight = 400;
	/**
	 * 是否是中文，显示倒计时可能需要加上天，日这样的文字
	 */
	public boolean Language;
	
	public void setFixedWordClr(int clr){
		this.FixedWordClr = clr;
	}
	public void setWordClr(int clr){
		this.WordClr = clr;
	}
	public void setBgClr(int clr){
		this.BgClr = clr;
	}
	public void setContent(String clr){
		this.Content = clr;
	}
	public void setItalic(int clr){
		this.Italic = clr;
	}
	public void setUnderline(int clr){
		this.Underline = clr;
	}
	public void setFaceName(int i){
		this.FaceName = i;
	}
	public void setHeight(int height){
		this.Height = height;
	}
	public void setWeight(int weight){
		this.Weight = weight;
	}
	public void setLanguage(boolean language){
		this.Language = language;
	}
	
	public int getFixedWordClr(){
		return FixedWordClr;
	}
	public int getWordClr(){
		return WordClr;
	}
	public int getBgClr(){
		return BgClr;
	}
	public int getItalic(){
		return Italic;
	}
	public int getUnderline(){
		return Underline;
	}
	public int getFaceName(){
		return FaceName;
	}
	public String getContent(){
		return Content;
	}
	public int getHeight(){
		return this.Height;
	}
	public int getWeight(){
		return this.Weight;
	}
	/**
	 * 是否是中文，显示倒计时可能需要加上天，日这样的文字
	 * @return
	 */
	public boolean getLanguage(){
		return this.Language;
	}
}
